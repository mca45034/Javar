package Inheritence;

public class Book {
	String b_name;
	String author_name;
	int price;
	public void setB_name(String b_name) {
		this.b_name=b_name;
	}
	public String getB_name() {
		return b_name;
	}
	public void setAuthor_name(String author_name) {
		this.author_name=author_name;
	}
	public String getAuthor_name() {
		return author_name;
	}
	public void setPrice(int price) {
		this.price=price;
	}
	public int getPrice() {
		return price;
	}
   
}
